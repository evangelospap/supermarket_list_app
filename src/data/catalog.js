function slugifyStarterName(name) {
  return name
    .toLocaleLowerCase("el-GR")
    .normalize("NFD")
    .replace(/\p{Diacritic}/gu, "")
    .replace(/[^a-z0-9α-ω]+/gu, "-")
    .replace(/^-|-$/g, "");
}

